class Solution:
    def depthSumInverse(self, nestedList: List[NestedInteger]) -> int:
        total, cur_total = 0 ,0
        que = deque(nestedList)
        while que:
            for i in range(len(que)):
                cur = que.popleft()
                if cur.isInteger():
                    cur_total += cur.getInteger()
                else:
                    que.extend(cur.getList())
            total += cur_total
        return total
